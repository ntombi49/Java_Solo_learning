// Variables

// 1️⃣ What is a Variable?
// A variable is a container that stores information so that a program can use it later.
// Think of a variable like a labeled box.

// Example:

// name → "Ntombi"
// age → 20
// The label is the variable name, and the value inside is the data.

// So in programming:
// variable name = stored value

// 2️⃣ Why Do We Use Variables?
// Variables allow us to:

// • store data
// • reuse data later
// • change values easily
// • perform calculations

// Without variables, programs would be very limited.

// Example without variables:

// System.out.println(20 + 5);
// Example with variables:

// int age = 20;
// System.out.println(age + 5);
// Now the program remembers the value.

// int age = 21;
// String name = "Ntombi";

// System.out.println(name);
// System.out.println(age);


// 3️⃣ Variable Syntax
// In Java, creating a variable follows this structure:

// dataType variableName = value;

// Example:
// int age = 20;

// | Part | Meaning       |
// | ---- | ------------- |
// | int  | data type     |
// | age  | variable name |
// | 20   | stored value  |

// 4️⃣ Common Data Types

// int age = 20;    stores whole numbers
// int marks = 75;

// double price = 19.99;   stores decimal numbers 
// double height = 1.75;

// char grade = 'A';        stores single characters

// String name = "Ntombi";          stores texts
// String city = "Johannesburg";


// 5️⃣ Printing Variables
// You can print variables using System.out.println.

// Example:

// String name = "Ntombi";
// System.out.println(name);

// Printing text + variables together

// String name = "Ntombi";
// System.out.println("Hello " + name);   concatenation

// 6️⃣ Updating Variables
// Variables can change value.

// Example:

// int age = 20;
// age = 21;
